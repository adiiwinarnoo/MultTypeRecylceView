package com.example.multitypeview.models;

public class Ads {

    private String adsTittle, ads;

    public Ads(String adsTittle, String ads) {
        this.adsTittle = adsTittle;
        this.ads = ads;
    }

    public String getAdsTittle() {
        return adsTittle;
    }

    public String getAds() {
        return ads;
    }
}
